#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	unsigned int	i;
	char			cc;

	cc = (char)c;
	i = 0;
	while (str[i])
	{
		if (str[i] == cc)
			return ((char *) &str[i]);
		i++;
	}
	if (str[i] == cc)
		return ((char *) &str[i]);
	return (NULL);
}
